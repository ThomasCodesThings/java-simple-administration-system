package base;

public class Country {

    private String continent;
    private String name;

    public void setName(String name) {
        this.name = name;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }

    public String getContinent() {
        return continent;
    }

    public String getName() {
        return name;
    }

    public Country(){

    }
}
